package Calculadora;

public class Operacion
{
    //1. Crear las siguiente Clase: Operaciones
	//1.1 Crear los siguientes metodos:
    //Suma, resta, multiplicacion y division (no sebe ser posible dividir por 0)
	//1.2 Realizar la implementacion de los metodos, es decir, debo poder sumar restar multiplicar y dividir

    public float sumar(float numero1, float numero2)
    {
        return numero1+numero2;
    }

    public float restar(float numero1, float numero2)
    {
        return numero1-numero2;
    }

    public float multiplicar(float numero1, float numero2)
    {
        return numero1*numero2;
    }
    public float dividir(float numero1, float numero2)
    {
        return numero1/numero2;
    }

}

